char	*ft_strrchr(const char *s, int c)
{
	int				i;
	unsigned char	x;

	x = c;
	i = 0;
	while (s[i])
		i++;
	while (i >= 0)
	{
		if (s[i] == x)
			return ((char *)(s + i));
		i--;
	}
	return (0);
}
